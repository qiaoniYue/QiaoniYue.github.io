/*全球热门游戏推荐，鼠标经过，出现border*/
/*游戏第一个*/
var mainGamesWrap1 = document.getElementById("main-gamesWrap1");
var mainGamesBox1 = document.getElementById("main-gamesBox1");
mainGamesBox1.onmouseover = function () {
    mainGamesWrap1.style.border = "1px solid #0078d7";
    mainGamesWrap1.style.borderTop = "0";
}
mainGamesBox1.onmouseout = function () {
    mainGamesWrap1.style.border = "1px solid #e8e8e8";
    mainGamesWrap1.style.borderTop = "0";
}
